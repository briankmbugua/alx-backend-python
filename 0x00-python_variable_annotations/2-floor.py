#!/usr/bin/env python3
import math
"""Module to return the floor of a floating point number
"""


def floor(n: float) -> int:
    """
    Calculate the floor of a floating-point number.

    Args:
        n (float): The input floating-point number.

    Returns:
        int: The floor of the input number as an integer.
    """
    return math.floor(n)
